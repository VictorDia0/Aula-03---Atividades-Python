#Crie um programa que receba a base e altura de um triangulo. Calcule e exiba a área conforme mostra a equação abaixo.

print ('Calculo da área do triangulo')
b = float(input('Digite o valor da base: '))
h = float(input('Digite o valor da altura: '))
area = (b * h)  / 2
print(f'A área do tringulo é {area}')
